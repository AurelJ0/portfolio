                <div class="card fondation-rothschild-card">
                    <h3>La Maison de Retraite et Gériatrique de la Fondation Rothschild</h3>
                    <p>La <strong>Maison de retraite et gériatrique</strong> ainsi que <strong>le réseau de la Fondation Rothschild</strong> en Île-de-France et au-delà ont pour objectif principal d’offrir des services de qualité pour les personnes âgées, en particulier celles qui ont des besoins spécifiques en matière de santé et de bien-être.</p>

                    <h4>1. Accueil et Hébergement des Personnes Âgées</h4>
                    <p>Les établissements de la Fondation Rothschild offrent un hébergement à long terme aux personnes âgées dépendantes, notamment celles en situation de perte d’autonomie physique ou cognitive, comme les personnes atteintes de maladies neurodégénératives (Alzheimer, Parkinson, etc.).</p>

                    <h4>2. Soins Médicaux et Suivi Personnalisé</h4>
                    <p>Les établissements disposent de services médicaux spécialisés pour répondre aux besoins de santé des résidents, avec une équipe pluridisciplinaire (médecins, infirmiers, psychologues, kinésithérapeutes, etc.). Un suivi médical personnalisé est proposé pour chaque résident.</p>

                    <h4>3. Maintien de la Qualité de Vie</h4>
                    <p>Les maisons de retraite sont centrées sur le maintien de la qualité de vie des résidents, en offrant un environnement agréable et sécurisé avec des activités de loisirs et des programmes de bien-être.</p>

                    <h4>4. Soutien aux Familles</h4>
                    <p>Les familles bénéficient d'un accompagnement et d’un soutien pour mieux comprendre les enjeux de la gériatrie, ainsi que des services de répit pour les aidants familiaux.</p>

                    <h4>5. Réseau Gériatrique en Île-de-France et Plus</h4>
                    <p>La Fondation Rothschild crée des <strong>réseaux gériatriques</strong> afin de renforcer l’interconnexion entre les différents acteurs du secteur médical et social, garantissant une prise en charge globale et continue.</p>

                    <h4>6. Innovation et Recherche en Gériatrie</h4>
                    <p>La Fondation soutient la recherche médicale dans le domaine de la <strong>gériatrie</strong>, en finançant des projets d'innovation pour améliorer les soins et l'autonomie des personnes âgées.</p>

                    <h4>7. Développement Durable et Accessibilité</h4>
                    <p>La Fondation veille à ce que ses établissements respectent des normes d’accessibilité et s’engage dans des initiatives de <strong>développement durable</strong> pour offrir un cadre de vie respectueux de l'environnement.</p>
